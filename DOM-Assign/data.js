function mouseOver() {
  let btn = document.getElementsByTagName('button')[0]
    btn.style.backgroundColor = 'blue'
}


function mouseOut() {
  let btn1 = document.getElementsByTagName('button')[1]
    btn1.style.backgroundColor = 'red'
}

